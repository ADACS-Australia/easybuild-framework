"""
Module for checking the terminal dimensions
copied from http://stackoverflow.com/questions/566746/how-to-get-console-window-width-in-python
"""

import os

def get_terminal_size():
    env = os.environ
    def ioctl_GWINSZ(fd):
        try:
            # these might fail because they are only available on Unix
            import fcntl, termios, struct, os
            cr = struct.unpack('hh', fcntl.ioctl(fd, termios.TIOCGWINSZ,
        '1234'))
        except:
            return
        return cr
    cr = ioctl_GWINSZ(0) or ioctl_GWINSZ(1) or ioctl_GWINSZ(2)
    if not cr:
        try:
            fd = os.open(os.ctermid(), os.O_RDONLY)
            cr = ioctl_GWINSZ(fd)
            os.close(fd)
        except:
            pass
    if not cr:
        cr = (env.get('LINES', 25), env.get('COLUMNS', 80))

    return int(cr[1]), int(cr[0])
